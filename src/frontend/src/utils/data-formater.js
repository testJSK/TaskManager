export default () => ({
	shortDate(date){

		console.log(date)
		if(!date){
			return ""
		}

		let result = date.getFullYear() + '-' +
     ( (date.getMonth() > 9) ? date.getMonth() : ("0" + date.getMonth()) ) + '-' +
     ( (date.getDate() > 9)  ? date.getDate()  : ("0" + date.getDate())  )

		return result;
	},
	curentDate(){
		let now = new Date();
		  
		return shortDate(now);
	}
})

